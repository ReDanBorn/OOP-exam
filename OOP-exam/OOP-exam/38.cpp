//-----------------------------Арифметика указателей и работа с массивами через указатели----------------------------------
#include <iostream>
using namespace std;

void main()
{
	system("chcp 1251");
	setlocale(LC_ALL, "RU");
	const int SIZE = 5;
	int arr[SIZE]{ 4, 6, 3, 7, 8 };
	for (int i = 0; i < SIZE; i++)
	{
		cout << arr[i] << endl;
	}
	cout << "Первый элемент\t" << /*!!!*/*arr/*!!!*/ << endl;//МАССИВ ЭТО УКАЗАТЕЛЬ!!!
	cout << "Второй элемент\t" << /*!!!*/*(arr + 1)/*!!!*/ << endl;//Алгебра указателей (i=0)+1=(i=1) это 2-й элемент массива
	cout << "Третий элемент\t" << /*!!!*/*(arr + 2)/*!!!*/ << endl;
	cout << "=====================================" << endl;
	int *pArr = arr; //Название массива = адрес первого элемента этого массива
	cout << "arr\t" << arr << endl;
	cout << "pArr\t" << pArr << endl;
	cout << "=====================================" << endl;
	for (int i = 0; i < SIZE; i++)
	{
		cout << *(pArr+i) << endl;//Пример арифметики указателей || "*" - это разыменовка адреса
	}
}
